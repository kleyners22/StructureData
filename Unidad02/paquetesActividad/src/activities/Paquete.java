package activities;

public class Paquete {
    private int id;
    private int codigoPostal;

    public Paquete(int id, int codigoPostal) {
        this.id = id;
        this.codigoPostal = codigoPostal;
    }


    public int getId() {return id;}
    public int getCodigoPostal() {return codigoPostal;}
}

